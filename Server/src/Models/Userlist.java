package Models;

/**
 *
 * @author franc
 */
public class Userlist {

    private int id;
    private int idGroup;
    private String idUser;

    public Userlist() {
    }

    public Userlist(int id, int idGroup, String idUser) {
        this.id = id;
        this.idGroup = idGroup;
        this.idUser = idUser;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdGroup() {
        return idGroup;
    }

    public void setIdGroup(int idGroup) {
        this.idGroup = idGroup;
    }

    public String getIdUser() {
        return idUser;
    }

    public void setIdUser(String idUser) {
        this.idUser = idUser;
    }
}
